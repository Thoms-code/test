#include <stdio.h>
#include <unistd.h>

void	ft_putchar(int i, int x, int y)
{
	int	j;

	j = 0;
	if (i == 0 || i == y - 1)
	{
		write (1, "o", 1);
		while (j < x - 2)
		{
			write(1, "-", 1);
			j++;
		}
		write(1, "o\n", 2);
	}
	else
	{
		write (1, "|", 1);
		while (j < x - 2)
		{
			write (1, " ", 1);
			j ++;
		}
		write(1, "|\n", 2);
	}
}
